<script>
import { useLocalStorage, StorageSerializers } from '@vueuse/core'
import { magentoGraphQL, combiningGraphQL } from '../fetch'

export default {
    props: {
        query: {
            type: String,
            required: true,
        },
        variables: {
            type: Object,
            default: () => ({}),
        },
        group: {
            // Group name for combining graphql queries, use "nameless" to join it with all others
            type: String,
        },
        check: {
            type: Function,
        },
        redirect: {
            type: String,
        },
        cache: {
            type: String,
        },
        callback: {
            type: Function,
        },
        errorCallback: {
            type: Function,
            default: (variables, error) => Notify(window.config.translations.errors.wrong, 'warning'),
        },
        store: {
            type: String,
            default: window.config.store_code,
        },
    },

    data: () => ({
        dataVariables: {},
        data: null,
        cachePrefix: 'graphql_',
        running: false,
    }),

    render() {
        if (!('default' in this.$slots)) {
            return null
        }

        return this.$slots.default({
            props: this.$props,
            variables: this.dataVariables,
            data: this.data,
            running: this.running,
            runQuery: this.runQuery,
        })
    },

    created() {
        this.dataVariables = this.variables

        if (!this.getCache()) {
            this.runQuery()
        }
    },

    methods: {
        getCache() {
            if (this.cache === undefined) {
                return false
            }
            this.data = useLocalStorage(this.cachePrefix + this.cache, null, { serializer: StorageSerializers.object }).value

            return this.data
        },

        async runQuery() {
            this.running = true
            try {
                let options = {
                    headers: {
                        Store: this.store,
                    },
                }

                let response = this.group
                    ? await combiningGraphQL(this.query, this.dataVariables, options, this.group)
                    : await magentoGraphQL(this.query, this.dataVariables, options)

                if (this.check) {
                    if (!this.check(response?.data)) {
                        this.running = false
                        Turbo.visit(window.url(this.redirect))
                        return false
                    }
                }

                this.data = this.callback ? await this.callback(this.dataVariables, response) : response.data

                if (this.cache) {
                    useLocalStorage(this.cachePrefix + this.cache, null, { serializer: StorageSerializers.object }).value = this.data
                }
            } catch (error) {
                console.error(error)
                this.errorCallback(this.dataVariables, await error?.response?.json())
            } finally {
                this.running = false
            }
        },
    },
}
</script>
